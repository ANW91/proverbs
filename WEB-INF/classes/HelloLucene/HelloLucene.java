    public static String[] HelloLucene(String arg) throws IOException, ParseException {
		String[] result, origins;
		String[] args = arg.split("\\s+");
        // 0. Specify the analyzer for tokenizing text.
        //    The same analyzer should be used for indexing and searching
		Directory index = new RAMDirectory();
		StandardAnalyzer analyzer = new StandardAnalyzer();
		IndexWriterConfig config = new IndexWriterConfig(analyzer);
		

        IndexWriter w = new IndexWriter(index, config);
        docs(w);
        w.close();

        // 2. query
		String querystr =args.length > 0? arg : "";

        // the "title" arg specifies the default field to use
        // when no field is explicitly specified in the query.
        Query q = new QueryParser("proverb", analyzer).parse(querystr);

        // 3. search
        int hitsPerPage = 500;
        IndexReader reader = DirectoryReader.open(index);
        IndexSearcher searcher = new IndexSearcher(reader);
        TopDocs docs = searcher.search(q, hitsPerPage);
        ScoreDoc[] hits = docs.scoreDocs;

        // 4. display results
		result = new String[(hits.length*2)+1];
        result[0] = hits.length + " results found";
		int k=1;
        for(int i=0;i<hits.length; i++) {
            int docId = hits[i].doc;
            Document d = searcher.doc(docId);
            result[k] = d.get("origin");
			result[k+1] = d.get("proverb");
			k+=2;
        }

        // reader can only be closed when there
        // is no need to access the documents any more.
        reader.close();
		return result;
    }
	
