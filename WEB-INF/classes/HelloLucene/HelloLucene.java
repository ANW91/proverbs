    private static void addDoc(IndexWriter w, String proverb, String origin) throws IOException {
        Document doc = new Document();
        doc.add(new TextField("proverb", proverb, Field.Store.YES));

        // use a string field for isbn because we don't want it tokenized
        doc.add(new StringField("origin", origin, Field.Store.YES));
        w.addDocument(doc);
    }
	
